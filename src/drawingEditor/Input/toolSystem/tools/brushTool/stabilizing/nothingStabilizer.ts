/* eslint-disable @typescript-eslint/no-empty-interface */
import { IncrementalStabilizer } from './stabilizer';
import { type BrushPoint } from '../brushTool';
import { type BaseBrushSettings } from '../../../settings/brushSettings';

////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////
//! TYPE DEFINITIONS
////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////

export interface NothingStabilizerSettings {
  type: 'nothing';
}

////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////
//! MAIN CLASS
////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////

export default class NothingStabilizer extends IncrementalStabilizer {
  private settings: NothingStabilizerSettings;
  private currentPoints: BrushPoint[];

  constructor(settings: NothingStabilizerSettings, _: BaseBrushSettings) {
    super('nothing');
    this.settings = settings;
    this.currentPoints = [];
  }

  addPoint(p: BrushPoint) {
    this.currentPoints.push(p);
  }

  getProcessedCurve(): BrushPoint[] {
    return this.currentPoints;
  }

  getProcessedCurveWithPoints(points: BrushPoint[]): BrushPoint[] {
    return points;
  }

  getRawCurve(): BrushPoint[] {
    return this.currentPoints;
  }

  update(deltaTime: number): void {
    throw new Error('Method not implemented.');
  }

  reset() {
    this.currentPoints = [];
  }
}
